    private void btnVerContaAgenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerContaAgenciaActionPerformed
        // TODO add your handling code here:

        if (txtNumeroAgenciaConta.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo em branco, digite o Numero da agencia.");
        } else {

            int numeroAgencia = 0;
            try {
                numeroAgencia = Integer.parseInt(txtNumeroAgenciaConta.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "");

            }
            
            

            if (visualizarContaPorAgenciaView == null) {
                visualizarContaPorAgenciaView = new VisualizarContaPorAgenciaView();

            }
            visualizarContaPorAgenciaView.setLocationRelativeTo(null);
            visualizarContaPorAgenciaView.setVisible(true);
            
            try {
                preencherTabelaContaPorAgencia(numeroAgencia);
            } catch (SQLException ex) {

            }

            

        }

        txtNumeroAgenciaConta.setText("");
    }//GEN-LAST:event_btnVerContaAgenciaActionPerformed

