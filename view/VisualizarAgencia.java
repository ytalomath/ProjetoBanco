    private void btnVerTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerTodosActionPerformed
        if (visualizarAgenciaView == null) {
            visualizarAgenciaView = new VisualizarAgenciaView();

        }
        visualizarAgenciaView.setLocationRelativeTo(null);
        visualizarAgenciaView.setVisible(true);

        try {
            preencherTabelaAgencia();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possível carregar a tabela.");
        }
    }//GEN-LAST:event_btnVerTodosActionPerformed

    private void btnVerUmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerUmActionPerformed
        // TODO add your handling code here:

        if (txtNumeroAgencia.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo em branco, digite o Numero da agencia.");
        } else {

            int numeroAgencia = 0;
            try {
                numeroAgencia = Integer.parseInt(txtNumeroAgencia.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "");

            }

            ControllerAgencia.buscarAgencia(numeroAgencia);
            txtNumeroAgencia.setText("");
        }
    }//GEN-LAST:event_btnVerUmActionPerformed

